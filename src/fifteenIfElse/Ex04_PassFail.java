package fifteenIfElse;

public class Ex04_PassFail
{
    public static void main(String[] args) {
        int marks = 45;
        if (marks >= 90) {
            System.out.println("Exellent");
        } else if (marks >= 50) {
            System.out.println("Pass");
        }
        else {
            System.out.println("Fail");

        }

    }
}
